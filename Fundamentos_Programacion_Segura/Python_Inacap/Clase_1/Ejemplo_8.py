# Solicite al usuario 3 números enteros e imprima el promedio de estos.

num_prom1 = float(input('Ingrese el primero número entero a calcular: '))
num_prom2 = float(input('Ingrese el segundo numero entero a calcular: '))
num_prom3 = float(input('Ingrese el tercer numero entero a calcular: '))

suma = num_prom1 + num_prom2 + num_prom3
promedio = suma / 3
print('El promedio de los 3 números ingresados corresponde a :', promedio)